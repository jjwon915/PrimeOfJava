package primary;

public class DataType {

	public static void main(String[] args) {
		
		char char1 = 'A';
		char char2 = 'B';
		System.out.println(char1);
		System.out.println(char2);
		
		byte byte1 = 1;
		System.out.println(byte1);
		
		short short1 = 30;
		System.out.println(short1);
		
		float float1 = 1.1f;
		System.out.println(float1);

	}

}
